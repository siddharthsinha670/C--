//Check whether a number is even or odd using if-else.


#include<iostream>
using namespace std;
int main()
{
    int a;
    cout<<("Enter the number:");
    cin>>a;
    if(a/2)
    {
        cout<<("the number is even");
    }
    else
    {
        cout<<("the number is odd.");
    }
    return 0;
}
